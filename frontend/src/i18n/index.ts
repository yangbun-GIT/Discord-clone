const dictionaries: Record<AppLanguage, Record<keyof typeof ko, string>> = {
  ko,
  en,
}

export type TranslationKey = keyof typeof ko

export function useI18n() {
  const preferences = usePreferencesStore()
  const language = computed(() => preferences.language)

  function t(key: TranslationKey, params: Record<string, string | number> = {}) {
    let value = dictionaries[preferences.language][key] ?? ko[key] ?? key
    for (const [paramKey, paramValue] of Object.entries(params)) {
      value = value.replaceAll(`{${paramKey}}`, String(paramValue))
    }
    return value
  }

  return {
    language,
    setLanguage: preferences.setLanguage,
    t,
  }
}
